# ======================
# TRAINING LOOP
# ======================
MAX_EPOCHS = 50

for epoch in range(MAX_EPOCHS):
    # ---- Train ----
    model.train()
    train_loss = 0.0

    for (ct, genes, clinical), (label, event) in train_loader:
        ct       = ct.float().to(device)
        genes    = genes.float().to(device)
        clinical = clinical.float().to(device)
        time     = label.float().to(device)
        event    = event.float().to(device)

        optimizer.zero_grad()
        output = model(ct, genes, clinical)
        loss   = cox_loss(output, time, event)
        loss.backward()

        # Fix: gradient clipping prevents exploding gradients from the
        # large gene Linear layer during early epochs.
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)

        optimizer.step()
        train_loss += loss.item()

    # ---- Validate ----
    model.eval()
    val_loss = 0.0
    all_preds, all_times, all_events = [], [], []

    with torch.no_grad():
        for (ct, genes, clinical), (label, event) in val_loader:
            ct       = ct.float().to(device)
            genes    = genes.float().to(device)
            clinical = clinical.float().to(device)
            time     = label.float().to(device)
            event    = event.float().to(device)

            output = model(ct, genes, clinical)
            loss   = cox_loss(output, time, event)

            val_loss += loss.item()
            all_preds.append(output.cpu())
            all_times.append(time.cpu())
            all_events.append(event.cpu())

    all_preds  = torch.cat(all_preds)
    all_times  = torch.cat(all_times)
    all_events = torch.cat(all_events)

    c_index = concordance_index(all_preds, all_times, all_events)

    print(
        f"Epoch {epoch+1:03d}/{MAX_EPOCHS} | "
        f"Train Loss: {train_loss:.4f} | "
        f"Val Loss: {val_loss:.4f} | "
        f"C-index: {c_index:.4f} | "
        f"LR: {optimizer.param_groups[0]['lr']:.2e}"
    )

    scheduler.step()

    # Save best model (based on C-index, not val loss)
    if c_index > best_cindex:
        best_cindex = c_index
        es_counter  = 0
        torch.save(model.state_dict(), "trained_models/best_lung_model.pth")
        print(f"  ✅ Best model saved (C-index: {best_cindex:.4f})")
    else:
        es_counter += 1
        if es_counter >= es_patience:
            print(f"  ⛔ Early stopping at epoch {epoch+1} (no improvement for {es_patience} epochs)")
            break

# ======================
# SAVE FINAL MODEL
# ======================
torch.save(model.state_dict(), "trained_models/lung_model.pth")
print(f"\nFinal model saved. Best C-index: {best_cindex:.4f}")

