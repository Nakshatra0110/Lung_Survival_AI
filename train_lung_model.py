# ======================
# MEDICALNET CT ENCODER
# Wraps the pretrained ResNet10 from MedicalNet (Tencent).
# Only layer1 + layer2 are used, giving a 128-channel spatial feature map.
# AdaptiveAvgPool3d collapses it to a 128-dim vector matching ct_out_dim.
# All weights are frozen — zero gradient updates during survival training.
# Falls back to the custom 4-block CNN if weights are not found.
# ======================
MEDNET_WEIGHTS = "trained_models/resnet_10_23dataset.pth"

class MedicalNetEncoder(nn.Module):
    """
    Multi-scale frozen ResNet10 encoder pretrained on 23 medical datasets.

    Improvement over the original single-scale encoder:
    Instead of using only layer1+layer2 (which captures only local texture
    at 128-dim), this encoder extracts features at TWO scales:

      Scale 1 — layer2 output (B, 128, 16, 16, 16):
        Local features: tumour boundary texture, spiculation,
        ground-glass opacity, small-scale intensity patterns.

      Scale 2 — layer4 output (B, 512, 4, 4, 4):
        Global features: tumour size relative to anatomy, lymph node
        involvement patterns, pleural contact, vascular invasion.

    Both are global-average-pooled and concatenated -> (B, 640).
    A small trainable projection maps this to (B, ct_out_dim=128).
    All ResNet weights remain frozen — zero overfitting risk from CT.

    Why multi-scale?
    A single scale forces a choice between local detail and global context.
    Multi-scale fusion gives the survival head information at both levels,
    letting the projection layer learn which scale is more prognostically
    relevant for each dimension of the output.
    """
    def __init__(self, weights_path, ct_out_dim=128):
        super().__init__()
        from resnet import resnet10

        base = resnet10(
            sample_input_W=128,
            sample_input_H=128,
            sample_input_D=128,
            shortcut_type="B",
            no_cuda=not torch.cuda.is_available(),
            num_seg_classes=2,
        )
        state = torch.load(weights_path, weights_only=False,
                           map_location="cpu")
        base.load_state_dict(state["state_dict"], strict=False)

        # Shared stem: conv1 -> bn1 -> relu -> maxpool
        self.stem = nn.Sequential(
            base.conv1, base.bn1, base.relu, base.maxpool
        )
        # Four residual stages kept separate so we can tap intermediate outputs
        self.layer1 = base.layer1   # (B, 64,  32, 32, 32)
        self.layer2 = base.layer2   # (B, 128, 16, 16, 16)  <- scale 1
        self.layer3 = base.layer3   # (B, 256,  8,  8,  8)
        self.layer4 = base.layer4   # (B, 512,  4,  4,  4)  <- scale 2

        self.pool = nn.AdaptiveAvgPool3d(1)

        # Freeze ALL encoder weights — no gradient flows through ResNet
        for p in (list(self.stem.parameters()) +
                  list(self.layer1.parameters()) +
                  list(self.layer2.parameters()) +
                  list(self.layer3.parameters()) +
                  list(self.layer4.parameters())):
            p.requires_grad = False

        # Trainable multi-scale projection
        # Concatenated dim: 128 (layer2) + 512 (layer4) = 640
        self.ms_proj = nn.Sequential(
            nn.Linear(128 + 512, ct_out_dim),
            nn.LayerNorm(ct_out_dim),
            nn.ReLU(inplace=True),
        )

    def forward(self, x):
        # x: (B, 1, 128, 128, 128)
        x = self.stem(x)       # (B, 64,  32, 32, 32)
        x = self.layer1(x)     # (B, 64,  32, 32, 32)
        f2 = self.layer2(x)    # (B, 128, 16, 16, 16)  <- local features
        f3 = self.layer3(f2)   # (B, 256,  8,  8,  8)
        f4 = self.layer4(f3)   # (B, 512,  4,  4,  4)  <- global features

        # Global average pool each scale -> (B, C)
        v2 = self.pool(f2).view(f2.size(0), -1)   # (B, 128)
        v4 = self.pool(f4).view(f4.size(0), -1)   # (B, 512)

        # Concatenate and project -> (B, ct_out_dim)
        ms = torch.cat([v2, v4], dim=1)            # (B, 640)
        return self.ms_proj(ms)                    # (B, 128)


# ======================
# MODEL
# Fix 1: CT encoder was severely underpowered (3,696 params vs 7.7M for genes).
#         Deepened to 4 conv blocks with InstanceNorm (stable at small batch sizes).
# Fix 2: Self-attention on 2 tokens (CT, gene) was equivalent to a learned
#         weighted average — replaced with a proper concat + MLP fusion block.
# Fix 3: Gene encoder now expects reduced gene_dim (run reduce_genes.py first).
#         With 500 genes the model drops from 7.7M to ~65K params, which is
#         appropriate for a dataset of this size.
# ======================
class MultimodalSurvivalModel(nn.Module):
    def __init__(self, gene_dim, latent_dim=32, ct_out_dim=128,
                 fusion_dim=128, clinical_dim=0):
        super().__init__()

        # --- CT branch ---
        # Use pretrained MedicalNet ResNet10 encoder if weights exist,
        # otherwise fall back to the custom 4-block CNN.
        # Both produce a (B, ct_out_dim) = (B, 128) output.
        self._ct_mode = "medicalnet"

        if os.path.exists(MEDNET_WEIGHTS):
            self.ct_encoder    = MedicalNetEncoder(MEDNET_WEIGHTS,
                                                   ct_out_dim=ct_out_dim)
            self._ct_is_frozen = True
            print("CT encoder: MedicalNet ResNet10 multi-scale (layer2+layer4, frozen)")
        else:
            self.ct_encoder = nn.Sequential(
                nn.Conv3d(1,  16, kernel_size=3, stride=2, padding=1),
                nn.InstanceNorm3d(16),
                nn.ReLU(inplace=True),
                nn.Conv3d(16, 32, kernel_size=3, stride=2, padding=1),
                nn.InstanceNorm3d(32),
                nn.ReLU(inplace=True),
                nn.Conv3d(32, 64, kernel_size=3, stride=2, padding=1),
                nn.InstanceNorm3d(64),
                nn.ReLU(inplace=True),
                nn.Conv3d(64, ct_out_dim, kernel_size=3, stride=2, padding=1),
                nn.InstanceNorm3d(ct_out_dim),
                nn.ReLU(inplace=True),
                nn.AdaptiveAvgPool3d(1),
            )
            self._ct_is_frozen = False
            print("CT encoder: custom 4-block CNN (trained from scratch)")

        # --- Gene branch (frozen autoencoder encoder) ---
        # The encoder was pretrained on 528 patients to compress
        # 2000 gene expression values into a 32-dim latent vector.
        # Its weights are loaded and frozen — the survival training
        # does not update them. A small trainable projection then
        # maps the 32-dim latent to ct_out_dim (128) to match the
        # CT branch output dimension entering the gated fusion.
        self.gene_encoder = nn.Sequential(
            nn.Linear(gene_dim, 256),
            nn.BatchNorm1d(256),
            nn.ReLU(inplace=True),
            nn.Dropout(p=0.2),
            nn.Linear(256, latent_dim),
        )

        # Trainable projection: latent -> ct_out_dim
        # This is the only part of the gene branch that gets updated
        # during survival training.
        self.gene_proj = nn.Sequential(
            nn.Linear(latent_dim, ct_out_dim),
            nn.ReLU(inplace=True),
        )

        # --- Fusion ---
        # Fix: replaced 2-token self-attention (= expensive 2x2 softmax) with
        # a gated fusion: each modality gets its own attention gate so the
        # network can learn how much to trust CT vs genomics per sample.
        self.ct_gate   = nn.Linear(ct_out_dim, ct_out_dim)
        self.gene_gate = nn.Linear(ct_out_dim, ct_out_dim)

        self.fusion = nn.Sequential(
            nn.Linear(ct_out_dim * 2, fusion_dim),
            nn.ReLU(inplace=True),
            nn.Dropout(p=0.3),
            nn.Linear(fusion_dim, 1)
        )

        # --- Clinical branch (optional) ---
        # A small MLP handles the 4-dim clinical vector. When clinical_dim=0
        # the branch is skipped entirely and fusion uses only CT + gene.
        self.clinical_dim = clinical_dim
        if clinical_dim > 0:
            self.clinical_encoder = nn.Sequential(
                nn.Linear(clinical_dim, 32),
                nn.ReLU(inplace=True),
                nn.Linear(32, ct_out_dim),
                nn.ReLU(inplace=True),
            )
            self.clin_gate = nn.Linear(ct_out_dim, ct_out_dim)
            # Fusion now takes 3x ct_out_dim instead of 2x
            self.fusion = nn.Sequential(
                nn.Linear(ct_out_dim * 3, fusion_dim),
                nn.ReLU(inplace=True),
                nn.Dropout(p=0.3),
                nn.Linear(fusion_dim, 1)
            )

        # Store modality attention weights for inspection
        self.last_ct_gate   = None
        self.last_gene_gate = None
        self.last_clin_gate = None

    def forward(self, ct, genes, clinical=None):
        # CT branch
        ct_feat = self.ct_encoder(ct.unsqueeze(1))     # (B, ct_out_dim)
        ct_feat = ct_feat.view(ct_feat.size(0), -1)         # (B, ct_out_dim)

        # Gene branch: frozen encoder -> trainable projection
        latent    = self.gene_encoder(genes)                 # (B, latent_dim)
        gene_feat = self.gene_proj(latent)                   # (B, ct_out_dim)

        # Gated fusion
        ct_w   = torch.sigmoid(self.ct_gate(ct_feat))       # (B, ct_out_dim)
        gene_w = torch.sigmoid(self.gene_gate(gene_feat))   # (B, ct_out_dim)

        self.last_ct_gate   = ct_w.detach()
        self.last_gene_gate = gene_w.detach()

        ct_gated   = ct_feat   * ct_w
        gene_gated = gene_feat * gene_w

        if self.clinical_dim > 0 and clinical is not None and clinical.shape[-1] > 0:
            clin_feat = self.clinical_encoder(clinical)          # (B, ct_out_dim)
            clin_w    = torch.sigmoid(self.clin_gate(clin_feat)) # (B, ct_out_dim)
            self.last_clin_gate = clin_w.detach()
            clin_gated = clin_feat * clin_w
            fused = torch.cat([ct_gated, gene_gated, clin_gated], dim=1)  # (B, 3*dim)
        else:
            self.last_clin_gate = None
            fused = torch.cat([ct_gated, gene_gated], dim=1)              # (B, 2*dim)

        return self.fusion(fused)                                          # (B, 1)


# ======================
# COX PARTIAL LIKELIHOOD LOSS
# Fix: original code divided by N (all samples) instead of the number of
# events, diluting the loss proportionally to censoring rate.
# ======================
def cox_loss(pred, time, event):
    pred  = pred.view(-1)
    time  = time.view(-1)
    event = event.view(-1)

    if event.sum() == 0:
        return torch.tensor(0.0, requires_grad=True, device=pred.device)

    # Sort by descending survival time (standard Cox convention)
    order = torch.argsort(time, descending=True)
    pred  = pred[order]
    event = event[order]

    hazard          = torch.exp(pred - pred.max())              # numerical stability
    log_cum_hazard  = torch.log(torch.cumsum(hazard, dim=0) + 1e-8)

    # Fix: sum only over events, divide by event count (not total N)
    event_terms = (pred - log_cum_hazard) * event
    return -event_terms.sum() / (event.sum() + 1e-8)


# ======================
# C-INDEX  (vectorised)
# Original was correct but O(n²) in Python loops.
# Replaced with a vectorised version — same result, much faster.
# ======================
def concordance_index(pred, time, event):
    pred  = pred.view(-1).float()
    time  = time.view(-1).float()
    event = event.view(-1).float()

    # All pairs (i, j) where patient i had event and died before j
    t_i = time.unsqueeze(1)     # (n, 1)
    t_j = time.unsqueeze(0)     # (1, n)
    p_i = pred.unsqueeze(1)
    p_j = pred.unsqueeze(0)
    e_i = event.unsqueeze(1)

    comparable = (t_i < t_j) & (e_i == 1)          # event occurred for i, j lived longer
    concordant = comparable & (p_i > p_j)
    tied_pred  = comparable & (p_i == p_j)

    num_pairs   = comparable.sum().float()
    num_correct = concordant.sum().float() + 0.5 * tied_pred.sum().float()

    return (num_correct / num_pairs).item() if num_pairs > 0 else 0.0


